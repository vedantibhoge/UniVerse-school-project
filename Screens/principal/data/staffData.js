export const getTeachersByClass = () => {
  const teachersByClass = {};
  staffData.forEach((teacher) => {
    teacher.classes.forEach((classRoom) => {
      if (!teachersByClass[classRoom]) {
        teachersByClass[classRoom] = [];
      }
      teachersByClass[classRoom].push(teacher);
    });
  });
  return teachersByClass;
};

export const getSortedClasses = (teachersByClass) => {
  return Object.keys(teachersByClass).sort((a, b) => {
    const gradeA = parseInt(a.split(' ')[1]);
    const gradeB = parseInt(b.split(' ')[1]);
    if (gradeA !== gradeB) return gradeA - gradeB;
    return a.localeCompare(b);
  });
};
