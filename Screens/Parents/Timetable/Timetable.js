const TagBadge = ({ label, color, bg, textWhite }) => (
  <View style={[styles.tag, { backgroundColor: bg || '#EFF6FF' }]}>
    <Text style={[styles.tagText, { color: textWhite ? '#fff' : color }]}>{label}</Text>
  </View>
);

const ClassCard = ({ item }) => {
  const isHighlight = !!item.highlight;
  return (
    <TouchableOpacity
      activeOpacity={0.75}
      style={[styles.classCard, isHighlight && styles.classCardHighlight]}
    >
      <View style={styles.classCardHeader}>
        <TagBadge
          label={item.tag}
          color={item.tagColor}
          bg={isHighlight ? 'rgba(255,255,255,0.25)' : undefined}
          textWhite={isHighlight}
        />
        {item.live && (
          <View style={styles.liveBadge}>
            <Text style={styles.liveText}>Live</Text>
          </View>
        )}
        <Text style={[styles.classTime, isHighlight && { color: 'rgba(255,255,255,0.85)' }]}>
          {item.time}
        </Text>
      </View>
      <Text style={[styles.classSubject, isHighlight && { color: '#fff' }]}>{item.subject}</Text>
      {!!item.teacher && (
        <Text style={[styles.classTeacher, isHighlight && { color: 'rgba(255,255,255,0.8)' }]}>
          👤 {item.teacher}
        </Text>
      )}
    </TouchableOpacity>
  );
};

