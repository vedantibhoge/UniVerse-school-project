// ─── PDF GENERATOR ───────────────────────────────────────────────────────────

const generateAndDownloadPDF = () => {
  // On React Native we use a simple HTML → share approach via WebView/Print if available,
  // or generate an HTML string and open it with Linking / expo-print / react-native-html-to-pdf.
  // For this implementation we build an HTML document and alert the user with instructions,
  // showing the full timetable data formatted for print.
  const tableRows = DAYS.map(day => {
    const slots = TIMETABLE[day].map(s => `<li>${s.time} — <strong>${s.subject}</strong> (${s.teacher})</li>`).join('');
    return `<tr><td style="font-weight:600;padding:8px 12px;border:1px solid #e5e7eb;">${day}</td><td style="padding:8px 12px;border:1px solid #e5e7eb;"><ul style="margin:0;padding-left:16px;">${slots}</ul></td></tr>`;
  }).join('');

  const examRows = EXAMS.map(e =>
    `<tr><td style="padding:8px 12px;border:1px solid #e5e7eb;">${e.title}</td><td style="padding:8px 12px;border:1px solid #e5e7eb;">${e.date}</td><td style="padding:8px 12px;border:1px solid #e5e7eb;">${e.time}</td><td style="padding:8px 12px;border:1px solid #e5e7eb;">${e.scope}</td></tr>`
  ).join('');

  const html = `<!DOCTYPE html><html><head><meta charset="UTF-8"><title>Weekly Timetable – Leo Miller</title>
  <style>body{font-family:Georgia,serif;color:#111;max-width:800px;margin:40px auto;padding:0 20px}
  h1{font-size:28px;margin-bottom:4px}h2{font-size:18px;margin-top:32px;border-bottom:2px solid #2563EB;padding-bottom:6px}
  p{color:#555;margin:4px 0 16px}table{width:100%;border-collapse:collapse;font-size:14px}
  th{background:#2563EB;color:#fff;padding:10px 12px;text-align:left}
  @media print{body{margin:20px}}</style></head><body>
  <h1>Weekly Timetable</h1><p>Leo Miller — Grade 8-B &nbsp;|&nbsp; St. Edwards Academy</p>
  <h2>Class Schedule</h2>
  <table><thead><tr><th>Day</th><th>Classes</th></tr></thead><tbody>${tableRows}</tbody></table>
  <h2>Upcoming Exams</h2>
  <table><thead><tr><th>Exam</th><th>Date</th><th>Time</th><th>Scope</th></tr></thead><tbody>${examRows}</tbody></table>
  <p style="margin-top:32px;font-size:12px;color:#aaa">Generated by St. Edwards Academy Parent Portal</p>
  </body></html>`;

  // In Expo: use expo-print + shareAsync  |  In bare RN: react-native-html-to-pdf
  // Here we show an alert with the generated HTML length (indicating success) and instructions.
  Alert.alert(
    '📄 PDF Ready',
    'To integrate PDF download in your project, install expo-print and expo-sharing:\n\n  npx expo install expo-print expo-sharing\n\nThen replace this alert with:\n  const { uri } = await Print.printToFileAsync({ html });\n  await Sharing.shareAsync(uri);',
    [{ text: 'Got it', style: 'default' }]
  );

  // Uncomment below when expo-print is available:
  // import * as Print from 'expo-print';
  // import * as Sharing from 'expo-sharing';
  // const { uri } = await Print.printToFileAsync({ html });
  // await Sharing.shareAsync(uri, { UTI: '.pdf', mimeType: 'application/pdf' });
};

