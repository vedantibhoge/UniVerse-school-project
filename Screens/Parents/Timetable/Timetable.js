// ─── MAIN SCREEN ─────────────────────────────────────────────────────────────

export default function WeeklyTimetable() {
  const [activeDay, setActiveDay] = useState('Wednesday');
  const scrollRef = useRef(null);

  const laptopLayout = SCREEN_WIDTH >= 768;

  return (
    <View style={styles.root}>
      <StatusBar barStyle="dark-content" backgroundColor="#F8FAFC" />
      <ScrollView
        ref={scrollRef}
        contentContainerStyle={[styles.container, laptopLayout && styles.containerLaptop]}
        showsVerticalScrollIndicator={false}
      >
        {/* ── HEADER ── */}
        <View style={[styles.headerRow, laptopLayout && styles.headerRowLaptop]}>
          <View>
            <Text style={styles.breadcrumb}>ACADEMICS  ›  TIMETABLE</Text>
            <Text style={styles.pageTitle}>Weekly Timetable</Text>
            <Text style={styles.subTitle}>
              Weekly Schedule for{' '}
              <Text style={styles.studentName}>Leo Miller</Text>
              {' '}— Grade 8-B
            </Text>
          </View>
          <TouchableOpacity
            activeOpacity={0.85}
            style={styles.downloadBtn}
            onPress={generateAndDownloadPDF}
          >
            <Text style={styles.downloadBtnIcon}>⬇</Text>
            <Text style={styles.downloadBtnText}>Download PDF</Text>
          </TouchableOpacity>
        </View>

        {/* ── DAY SELECTOR ── */}
        <ScrollView
          horizontal
          showsHorizontalScrollIndicator={false}
          contentContainerStyle={styles.dayTabsRow}
        >
          {DAYS.map(day => {
            const active = day === activeDay;
            return (
              <TouchableOpacity
                key={day}
                activeOpacity={0.8}
                style={[styles.dayTab, active && styles.dayTabActive]}
                onPress={() => setActiveDay(day)}
              >
                <Text style={[styles.dayTabText, active && styles.dayTabTextActive]}>
                  {day.toUpperCase()}
                </Text>
              </TouchableOpacity>
            );
          })}
        </ScrollView>

        {/* ── SCHEDULE GRID ── */}
        {laptopLayout ? (
          // Desktop: show all days in a horizontal grid
          <View style={styles.laptopGrid}>
            {DAYS.map(day => (
              <View key={day} style={styles.laptopDayColumn}>
                <Text style={[
                  styles.laptopDayHeader,
                  day === activeDay && styles.laptopDayHeaderActive,
                ]}>
                  {day.toUpperCase()}
                </Text>
                {TIMETABLE[day].map((item, idx) => (
                  <ClassCard key={idx} item={item} />
                ))}
              </View>
            ))}
          </View>
        ) : (
          // Mobile: show selected day
          <View style={styles.mobileSchedule}>
            <Text style={styles.mobileDayLabel}>{activeDay}</Text>
            {TIMETABLE[activeDay].map((item, idx) => (
              <ClassCard key={idx} item={item} />
            ))}
          </View>
        )}

        {/* ── EXAMS + ADVISORY ── */}
        <View style={[styles.bottomSection, laptopLayout && styles.bottomSectionLaptop]}>
          {/* Upcoming Exams */}
          <View style={[styles.examsPanel, laptopLayout && styles.examsPanelLaptop]}>
            <View style={styles.examsPanelHeader}>
              <Text style={styles.examsPanelTitle}>Upcoming Exams</Text>
              <View style={styles.pendingBadge}>
                <Text style={styles.pendingBadgeText}>3 Tests Pending</Text>
              </View>
            </View>
            <ScrollView
              horizontal={!laptopLayout}
              showsHorizontalScrollIndicator={false}
              contentContainerStyle={laptopLayout ? styles.examCardsColumnWrap : styles.examCardsRow}
            >
              {EXAMS.map((exam, i) => <ExamCard key={i} exam={exam} />)}
            </ScrollView>
          </View>

          {/* Advisory */}
          <TouchableOpacity activeOpacity={0.9} style={[styles.advisoryCard, laptopLayout && styles.advisoryCardLaptop]}>
            <View style={styles.advisoryIconWrap}>
              <Text style={styles.advisoryIcon}>!</Text>
            </View>
            <Text style={styles.advisoryTitle}>Exam Advisory</Text>
            <Text style={styles.advisoryBody}>
              The Science midterm is next week. Please ensure Leo has reviewed the lab safety protocols and the chemical reaction charts available in his study guide.
            </Text>
            <TouchableOpacity activeOpacity={0.85} style={styles.studyGuideBtn}>
              <Text style={styles.studyGuideBtnText}>View Study Guide</Text>
            </TouchableOpacity>
          </TouchableOpacity>
        </View>
      </ScrollView>
    </View>
  );
}

